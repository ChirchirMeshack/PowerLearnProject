-- Question 1: Write an SQL query to create a new database called salesDB.

CREATE DATABASE salesDB;

-- Explanation: This command instructs the SQL database management system
-- to create a new database schema with the specified name 'salesDB'.


-- Question 2: Write an SQL query to drop (delete) the database called demo.

DROP DATABASE demo;

-- Explanation: This command instructs the SQL database management system
-- to permanently delete the database named 'demo' and all its contents
-- (tables, data, procedures, etc.).